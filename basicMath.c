float Add(float x , float y) {
 float result;
 result = x+y;
 return result; 
}


float Sub(float x , float y) {
 float result;
 result = x-y;
 return result; 
}


double Mul(double x , int y) {
double result;
 result = x*y;
 return result;
}


double Div(double x, int y) {
double result;
 result = x/y;
 return result;
}





